package objetosBigDecimal;


public class ejercicio2 {
public static void main(String []args) {
		
		Float numero1 = 10.7f;
		Float numero2 = 5.4f;
		
		
		System.out.println(numero1+numero2);
		System.out.println(numero1-numero2);
		System.out.println(numero1*numero2);
		System.out.println(numero1/numero2);
		
	}
}
